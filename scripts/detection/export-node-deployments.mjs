#!/usr/bin/env node
import fs from "node:fs/promises";
import path from "node:path";

const rootDir = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..", "..");
const apiUrl = (process.env.DETECTION_API_URL || "http://127.0.0.1:3001").replace(/\/$/, "");
const outputComposePath = path.resolve(rootDir, process.env.DETECTION_OUTPUT_COMPOSE || "infra/docker-compose.detection.generated.yml");
const nodeFilter = new Set(
  (process.env.DETECTION_NODE_IDS || "")
    .split(",")
    .map((value) => value.trim())
    .filter(Boolean)
);

function extractContainerPort(definition) {
  try {
    const url = new URL(definition.env?.NODE_ENDPOINT || definition.endpoint || "");
    return url.port || (url.protocol === "https:" ? "443" : "80");
  } catch {
    const firstPort = Array.isArray(definition.ports) ? definition.ports[0] : "";
    if (typeof firstPort === "string" && firstPort.includes(":")) {
      return firstPort.split(":").at(-1) || "8091";
    }
    return "8091";
  }
}

function yamlScalar(value) {
  if (value === null || value === undefined) return "null";
  if (typeof value === "number" || typeof value === "boolean") return String(value);
  return JSON.stringify(String(value));
}

function toYaml(value, indent = 0) {
  const prefix = " ".repeat(indent);
  if (Array.isArray(value)) {
    if (value.length === 0) return `${prefix}[]`;
    return value
      .map((item) => {
        if (item && typeof item === "object") {
          const nested = toYaml(item, indent + 2);
          const [firstLine, ...rest] = nested.split("\n");
          return `${prefix}- ${firstLine.trimStart()}${rest.length ? `\n${rest.join("\n")}` : ""}`;
        }
        return `${prefix}- ${yamlScalar(item)}`;
      })
      .join("\n");
  }
  if (value && typeof value === "object") {
    const entries = Object.entries(value);
    if (entries.length === 0) return `${prefix}{}`;
    return entries
      .map(([key, item]) => {
        if (item && typeof item === "object") {
          return `${prefix}${key}:\n${toYaml(item, indent + 2)}`;
        }
        return `${prefix}${key}: ${yamlScalar(item)}`;
      })
      .join("\n");
  }
  return `${prefix}${yamlScalar(value)}`;
}

async function requestJson(url, options = {}) {
  const response = await fetch(url, options);
  const bodyText = await response.text();
  let body = null;
  try {
    body = bodyText ? JSON.parse(bodyText) : null;
  } catch {
    body = null;
  }
  if (!response.ok) {
    throw new Error(`${options.method || "GET"} ${url} failed (${response.status}): ${bodyText}`);
  }
  return body;
}

async function resolveAuthToken() {
  if (process.env.DETECTION_AUTH_TOKEN) return process.env.DETECTION_AUTH_TOKEN;
  const email = process.env.DETECTION_ADMIN_EMAIL || "admin@nearhome.dev";
  const password = process.env.DETECTION_ADMIN_PASSWORD;
  if (!password) {
    throw new Error("DETECTION_AUTH_TOKEN or DETECTION_ADMIN_PASSWORD is required");
  }
  const body = await requestJson(`${apiUrl}/auth/login`, {
    method: "POST",
    headers: { "content-type": "application/json" },
    body: JSON.stringify({ email, password })
  });
  const token = body?.accessToken || body?.token;
  if (!token || typeof token !== "string") {
    throw new Error("auth/login did not return a token");
  }
  return token;
}

async function main() {
  const token = await resolveAuthToken();
  const headers = { Authorization: `Bearer ${token}` };
  const nodeList = await requestJson(`${apiUrl}/ops/nodes?sync=0`, { headers });
  const nodes = Array.isArray(nodeList?.data) ? nodeList.data : [];
  const selectedNodes = nodeFilter.size > 0 ? nodes.filter((node) => nodeFilter.has(node.nodeId)) : nodes;

  if (selectedNodes.length === 0) {
    throw new Error(nodeFilter.size > 0 ? "No matching nodes found for DETECTION_NODE_IDS" : "No nodes available in control-plane");
  }

  const definitions = [];
  for (const node of selectedNodes) {
    const payload = await requestJson(`${apiUrl}/ops/nodes/${encodeURIComponent(node.nodeId)}/deploy-definition`, { headers });
    if (payload?.data) definitions.push(payload.data);
  }

  if (definitions.length === 0) {
    throw new Error("No deploy definitions returned for selected nodes");
  }

  const composeServices = {};
  for (const definition of definitions) {
    const containerPort = extractContainerPort(definition);
    for (const [serviceName, service] of Object.entries(definition.composeService || {})) {
      const normalizedService = {
        ...service,
        expose: [containerPort],
        restart: "unless-stopped",
        networks: {
          nearhome_net: {
            aliases: [definition.nodeId]
          }
        }
      };
      delete normalizedService.ports;
      composeServices[serviceName] = normalizedService;
    }
  }

  const document = [
    "# Generated by scripts/detection/export-node-deployments.mjs",
    `# generatedAt: ${new Date().toISOString()}`,
    `# apiUrl: ${apiUrl}`,
    `# nodeIds: ${definitions.map((definition) => definition.nodeId).join(",")}`,
    "services:",
    toYaml(composeServices, 2),
    ""
  ].join("\n");

  await fs.mkdir(path.dirname(outputComposePath), { recursive: true });
  await fs.writeFile(outputComposePath, document, "utf8");

  const warningCount = definitions.reduce(
    (total, definition) => total + (Array.isArray(definition.warnings) ? definition.warnings.length : 0),
    0
  );
  console.log(`Wrote ${outputComposePath}`);
  console.log(`Generated services: ${Object.keys(composeServices).join(", ")}`);
  if (warningCount > 0) {
    console.log(`Warnings detected across deploy definitions: ${warningCount}`);
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
